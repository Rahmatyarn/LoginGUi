package practice;

public class ForLoopExample {

	public static void main(String[] args) {
		// Loops can execute a block of code as long as a specified condition is reached. 
		// We use loops because they save time, reduce errors, and they make code more readable.
		
		// When you know exactly how many times you want to loop through a block of code
		// use the for loop instead of a while loop
		
		// Syntax 
		// for ( Declaration; conditions; update) {
		// Block of code to be executed. 
	 //	}
		   for (int i = 0; i <= 10; i++) {
			   System.out.println(i);
		   }
		// in Declaration we declare a variable.
		   // in Condition we define the condition for executing the code block
		   // in update we are define what code to be executed after each iteration

	}

}
