package practice;

public class Assignment {

	public static void main(String[] args) {
		
		//Assignment Operator
		
		int x = 10;
		
		x += 5 ;  // This means x(10) + 5
		System.out.println(x);
		System.out.println(x + 5);
		
		// Increment  ++ 
		// Decrement  --
		int y = 1;
		System.out.println(1);
		System.out.println(++y);
		System.out.println(--y);
		System.out.println(y);
		
		
		
		

	}

}
